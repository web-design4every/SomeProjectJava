    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
        if (checkExist(txtAccount.getText())) {
            JOptionPane.showMessageDialog(null, "Accounts is existed !!");
            return;
        }
        try {
            //Tao cau lenh sql update data
            String gender="";
            if (rbtnMale.isSelected()) {
               gender+=true;
           } else {
               gender+=false;
           }
            if (txtAccount.getText().isEmpty()||txtPassword.getText().isEmpty()||txtAddress.getText().isEmpty()||txtDob.getText().isEmpty()||txtRePassword.getText().isEmpty()||txtName.getText()==""||(!rbtnFemale.isSelected()&&!rbtnMale.isSelected())) {
               JOptionPane.showMessageDialog(null, "Must input full of information");
           } else if ((!txtPassword.getText().equals(txtRePassword.getText()))&&(!txtPassword.getText().isEmpty())) {
               JOptionPane.showMessageDialog(null, "Wrong re-password");
           }else{
            String date="";
            if (kiemtraDate()==true) {
                date=txtDob.getText();
            String strAdd = "insert into tblUser values ( '"+txtAccount.getText()+"', '"+txtPassword.getText()+"', '"+txtName.getText()+"', '"+gender+"', '"+txtAddress.getText()+"', '"+date+"')";
            stm.execute(strAdd);
            JOptionPane.showMessageDialog(null, "Add successfully");
            }else{
                JOptionPane.showMessageDialog(null, "Loi dinh dang ngay thang");
            }
           }
        } catch (Exception e) {
            System.out.println("Loi Add: "+e.getMessage());
        } 
    }//GEN-LAST:event_btnRegisterActionPerformed

