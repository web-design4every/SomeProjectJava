    private void btnOkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOkActionPerformed
        try {
            int a = Integer.parseInt(txta.getText());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Wrong integer type input for a");
        }
        try {
            int b = Integer.parseInt(txtb.getText());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Wrong integer type input for b");
        }
        int a = Integer.parseInt(txta.getText());
        int b = Integer.parseInt(txtb.getText());
        double c;
        if (rbtn1.isSelected()) {
            if (a == 0) {
                if (b == 0) {
                    lblResult.setText("Phương trình này có vô số nghiệm.");
                } else {
                    lblResult.setText("Phương trình vô nghiệm.");
                }
            } else {
                c = (double) -b / a;
                lblResult.setText(String.valueOf(c));
            }
        }
        if (rbtn2.isSelected()) {
            lblResult.setText(String.valueOf(USCLN(a, b)));
        }
        if (rbtn3.isSelected()) {
            lblResult.setText(String.valueOf(BSCNN(a, b)));
        }
    }//GEN-LAST:event_btnOkActionPerformed

